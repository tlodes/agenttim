"""MCPAgentBench test case loader.
Loads converted MCPAgentBench test cases from JSON files generated by
scripts/convert_mcpagentbench.py.  Each JSON entry is validated against
MCPAgentBenchTestCase (Pydantic) at load time — missing fields, wrong
types, or inconsistent tool/execution names raise immediately instead of
crashing mid-evaluation.
Output format per case:
    {
        "id": "MCPAB-daytask_1_tool_18",
        "source_id": "daytask_1_tool_18",
        "source_category": "daytasks_single",
        "dimension": "STurn-1T",
        "dataset": "MCPAgentBench",
        "category": "single",              # single | parallel | sequential
        "input": "The user wants to know the latest status of USPS package...",
        "expected_tool_calls": [
            {"name": "package_delivery_tracker", "arguments": {"tracking_number": "..."}}
        ],
        "expected_execution": [["package_delivery_tracker"]],
        "available_functions": [
            {"name": "package_delivery_tracker", "description": "...", "parameters": {...}}
        ]
    }
Usage:
    from agenttim.evaluation.test_cases.mcpagentbench_loader import (
        MCPAB_ST_1T_DAYTASKS,
        MCPAB_ST_MT_DAYTASKS_PARALLEL,
        MCPAB_ALL,
    )
"""
import json
import sys
from pathlib import Path
from typing import Any
from pydantic import ValidationError
from agenttim.evaluation.test_cases.schemas import MCPAgentBenchTestCase
DATA_DIR = Path(__file__).parent / "mcpagentbench_data"
def _load(filename: str) -> list[dict[str, Any]]:

    """Load and validate MCPAgentBench test cases from a JSON file."""

    path = DATA_DIR / filename

    if not path.exists():

        return []

    with open(path, encoding="utf-8") as f:

        raw_items = json.load(f)

    validated: list[dict[str, Any]] = []

    errors: list[str] = []

    for i, item in enumerate(raw_items):

        try:

            tc = MCPAgentBenchTestCase.model_validate(item)

            validated.append(tc.to_dict())

        except ValidationError as e:

            item_id = item.get("id", f"index {i}")

            errors.append(f"  [{item_id}] {e}")

    if errors:

        print(

            f"WARNING: {len(errors)} validation error(s) in {filename}:\n"

            + "\n".join(errors),

            file=sys.stderr,

        )

    return validated
def load_tool_schemas() -> dict[str, Any]:

    """Load all MCPAgentBench tool schemas (141 MCP server definitions)."""

    path = DATA_DIR / "tool_schemas.json"

    if not path.exists():

        return {}

    with open(path, encoding="utf-8") as f:

        return json.load(f)
